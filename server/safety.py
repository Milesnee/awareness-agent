"""安全兜底：危机信号检测 → 转介话术。

产品红线：小澄是觉察引导工具，不是心理诊疗服务。
检测到危机信号时不继续引导流程，输出固定转介内容。
关键词命中偏保守（宁可多触发），后续可升级为 LLM 分类。
"""
from __future__ import annotations

CRISIS_KEYWORDS = [
    "自杀", "想死", "不想活", "活不下去", "结束生命", "轻生",
    "自残", "割腕", "伤害自己", "了结",
]

REFERRAL_MESSAGE = (
    "听到你现在很难受。我想先暂停今天的觉察引导，因为你此刻值得真人的专业支持。\n\n"
    "可以现在就联系：\n"
    "· 全国心理援助热线：12356（24小时）\n"
    "· 北京心理危机研究与干预中心：010-82951332\n"
    "· 紧急情况请拨 120 或 110\n\n"
    "我会一直在这里。等你想聊的时候，随时回来。"
)


def check(text: str) -> bool:
    return any(k in text for k in CRISIS_KEYWORDS)
